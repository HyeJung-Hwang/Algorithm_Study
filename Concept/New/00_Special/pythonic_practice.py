# 테스트 코드
# =============================================================================
def run_tests():
    passed = 0
    failed = 0
    total = 0

    def check(test_name, actual, expected):
        nonlocal passed, failed, total
        total += 1
        if actual == expected:
            passed += 1
            print(f"  ✅ {test_name}")
        else:
            failed += 1
            print(f"  ❌ {test_name}")
            print(f"     Expected: {expected}")
            print(f"     Got:      {actual}")

    # 문제 1: Unpacking
    print("\n[문제 1] Unpacking")
    check("기본", exclude_first_last([1, 2, 3, 4, 5]), [2, 3, 4])
    check("2개 원소", exclude_first_last([1, 2]), [])
    check("많은 원소", exclude_first_last([10, 20, 30, 40, 50, 60]), [20, 30, 40, 50])

    # 문제 2: List Comprehension 기본
    print("\n[문제 2] List Comprehension (기본)")
    check("n=10", multiples_of_three(10), [3, 6, 9])
    check("n=15", multiples_of_three(15), [3, 6, 9, 12, 15])
    check("n=2", multiples_of_three(2), [])

    # 문제 3: 조건부 값 변환
    print("\n[문제 3] List Comprehension (클리핑)")
    check("기본", clip_values([3, 7, 15, 20, 5], 10), [3, 7, 10, 10, 5])
    check("모두 이하", clip_values([1, 2, 3], 5), [1, 2, 3])
    check("모두 초과", clip_values([10, 20, 30], 5), [5, 5, 5])

    # 문제 4: Set 중복 제거
    print("\n[문제 4] Set 중복 제거")
    check("기본", unique_lowercase_sorted(['Apple', 'BANANA', 'apple', 'Cherry', 'banana']),
          ['apple', 'banana', 'cherry'])
    check("이미 유니크", unique_lowercase_sorted(['x', 'y', 'z']), ['x', 'y', 'z'])

    # 문제 5: Dictionary 생성
    print("\n[문제 5] Dictionary (zip)")
    check("기본", make_dict(['a', 'b', 'c'], [1, 2, 3]), {'a': 1, 'b': 2, 'c': 3})
    check("빈 입력", make_dict([], []), {})

    # 문제 6: defaultdict
    print("\n[문제 6] defaultdict")
    result = group_scores([('Alice', 90), ('Bob', 80), ('Alice', 95)])
    check("기본", dict(result) if result else None, {'Alice': [90, 95], 'Bob': [80]})

    # 문제 7: 정렬
    print("\n[문제 7] 정렬 (lambda)")
    check("기본", custom_sort([(1, 3), (8, 2), (5, 3), (2, 5)]),
          [(8, 2), (5, 3), (1, 3), (2, 5)])
    check("동일값", custom_sort([(3, 1), (1, 1), (2, 1)]),
          [(3, 1), (2, 1), (1, 1)])

    # 문제 8: strip
    print("\n[문제 8] strip")
    check("대시 제거", strip_chars('---hello---', '-'), 'hello')
    check("복합 문자", strip_chars('##**title**##', '#*'), 'title')

    # 문제 9: 문자열 뒤집기
    print("\n[문제 9] 문자열 뒤집기")
    check("기본", reverse_string('hello'), 'olleh')
    check("빈 문자열", reverse_string(''), '')
    check("회문", reverse_string('racecar'), 'racecar')

    # 문제 10: 조합 최대곱
    print("\n[문제 10] 조합 최대곱")
    check("양수만", max_product_of_three([1, 2, 3, 4, 5]), 60)
    check("음수 포함", max_product_of_three([-10, -9, 1, 2, 3]), 270)
    check("모두 음수", max_product_of_three([-5, -4, -3, -2, -1]), -6)

    # 문제 11: join
    print("\n[문제 11] join")
    check("기본", join_with_arrow([1, 2, 3, 4]), "1 -> 2 -> 3 -> 4")
    check("단일 원소", join_with_arrow([42]), "42")

    # 문제 12: enumerate
    print("\n[문제 12] enumerate")
    check("기본", find_all_indices([1, 3, 5, 3, 7, 3], 3), [1, 3, 5])
    check("없는 값", find_all_indices([1, 2, 3], 5), [])
    check("모두 같은 값", find_all_indices([7, 7, 7], 7), [0, 1, 2])

    # 문제 13: Counter
    print("\n[문제 13] Counter")
    check("기본", top_k_chars('hello world', 2), [('l', 3), ('o', 2)])
    check("k=1", top_k_chars('aabbcc', 1), [('a', 2)])

    # 문제 14: 배열 회전
    print("\n[문제 14] 배열 회전")
    check("3x3", rotate_90([[1, 2, 3], [4, 5, 6], [7, 8, 9]]),
          [(7, 4, 1), (8, 5, 2), (9, 6, 3)])
    check("2x2", rotate_90([[1, 2], [3, 4]]),
          [(3, 1), (4, 2)])

    # 문제 15: 종합
    print("\n[문제 15] 종합 문제")
    check("기본", rank_students({'Alice': [90, 85, 92], 'Bob': [78, 88, 76], 'Charlie': [95, 90, 100]}),
          [('Charlie', 95.0), ('Alice', 89.0), ('Bob', 80.7)])

    # 결과 요약
    print(f"\n{'='*50}")
    print(f"결과: {passed}/{total} 통과 ({failed}개 실패)")
    if failed == 0:
        print("축하합니다! 모든 테스트를 통과했습니다! 🎉")
    print(f"{'='*50}")


if __name__ == '__main__':
    run_tests()
